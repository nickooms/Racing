function url2Tile(url) {
	//alert(5)
	var bbox = url.split('BBOX=')[1].split('&')[0];
	var tileTexture = tileTextures[bbox];
	if (tileTexture != null) {
		//doubleTiles++;
		//console.log(doubleTiles);
		return;
	}
	//alert(bbox);
	tileTexture = addTexture('tiles/' + bbox + '.png');
	tileTextures[bbox] = tileTexture;
	var tileMaterial = new THREE.MeshPhongMaterial({
		map: tileTextures[bbox],
		color: 0xff0000,
		ambient: 0x777777,
		specular: 0xFF6666,
		shininess: 50,
		shading: THREE.SmoothShading
	});
	tileMaterials[bbox] = tileMaterial;
	var coords = bbox.split(',');
	for (var i = 0; i < coords.length; i++) {
		coords[i] = coords[i].substr(0, 9);
	}
	addFinish([[coords[2], coords[1]],[coords[2], coords[3]],[coords[0], coords[3]],[coords[0], coords[1]]], 'Tile', bbox);
	//url2Tile('http://grb.agiv.be/GRB-raadpleegdienst/wms?LAYERS=GRB_BASISKAART&FORMAT=png&SERVICE=WMS&VERSION=1.1.1&REQUEST=GetMap&STYLES=&EXCEPTIONS=application%2Fvnd.ogc.se_inimage&SRS=EPSG%3A31370&BBOX=152499.7814,221910.8477,152511.0314,221922.0977&WIDTH=256&HEIGHT=256');
}
function addTiles(tileGroup) {
	tileGroup + tileGroup.pop();
	for (var i = 0; i < TILES.length; i++) {
		var tile = TILES[i];
		if (
			(tileGroup[0] <= tile[0] && tileGroup[2] >= tile[0]) &&
			(tileGroup[0] <= tile[2] && tileGroup[2] >= tile[2]) &&
			(tileGroup[1] <= tile[1] && tileGroup[3] >= tile[1]) &&
			(tileGroup[1] <= tile[3] && tileGroup[3] >= tile[3])
		) {
			url2Tile('BBOX=' + tile);
		}
	}
}
